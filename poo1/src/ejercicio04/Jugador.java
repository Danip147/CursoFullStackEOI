package ejercicio04;

public class Jugador {
	
	private String nombre;
	private int sueldo;
	
	
	public Jugador() {
		
	}


	public Jugador(String nombre, int sueldo) {
		super();
		this.nombre = nombre;
		this.sueldo = sueldo;
	}


	public String getNombre() {
		return nombre;
	}


	public void setNombre(String nombre) {
		this.nombre = nombre;
	}


	public int getSueldo() {
		return sueldo;
	}


	public void setSueldo(int sueldo) {
		this.sueldo = sueldo;
	}
	
	
	
	
	
	
	
	
}

